    <section class="faq-content">
        <h1 class="faq-title">FAQ</h1>
        <p class="faq-intro">
            Vous trouverez ici les réponses aux questions les plus posées sur l'expérience.
            Pour toute autre demande, l'équipe reste à votre disposition.
        </p>

        <div class="faq-block">
            <h2 class="faq-block-title">Sécurité &amp; bien-être</h2>

            <details class="faq-item">
                <summary>L'expérience est-elle sécurisée ?</summary>
                <p>Oui. Même si l'ambiance est mystérieuse et immersive, l'expérience reste encadrée par une équipe présente sur place pendant toute la durée de l'événement. Les participants évoluent dans un cadre contrôlé, avec des consignes données dès leur arrivée.</p>
            </details>

            <details class="faq-item">
                <summary>Y a-t-il vraiment des moments de peur ?</summary>
                <p>L'expérience repose surtout sur la tension, le mystère, l'infiltration et les interactions avec les figurants. Le but n'est pas de traumatiser les participants, mais de créer une ambiance inquiétante et immersive.</p>
            </details>

            <details class="faq-item">
                <summary>Peut-on arrêter l'expérience si on ne se sent pas bien ?</summary>
                <p>Oui. Un système de sécurité est prévu afin qu'un participant puisse prévenir l'équipe s'il souhaite faire une pause ou quitter l'expérience. L'immersion est importante, mais la sécurité et le confort des participants restent prioritaires.</p>
            </details>
        </div>

        <div class="faq-block">
            <h2 class="faq-block-title">Déroulement de l'expérience</h2>

            <details class="faq-item">
                <summary>Quelles activités sont prévues ?</summary>
                <p>L'expérience comprend plusieurs phases : accueil immersif, enquête, observation, interactions avec les membres du Cercle, activités scénarisées, recherche d'indices et escape game final.</p>
            </details>

            <details class="faq-item">
                <summary>Combien de temps dure l'expérience ?</summary>
                <p>L'expérience dure environ 18 heures. Elle se déroule principalement en soirée et de nuit afin de renforcer l'ambiance mystérieuse du Domaine.</p>
            </details>

            <details class="faq-item">
                <summary>Où se déroule l'expérience ?</summary>
                <p>L'expérience se déroule à Piney, dans l'Aube, en pleine nature. L'adresse exacte et les informations pratiques sont communiquées sur notre site.</p>
            </details>

            <details class="faq-item">
                <summary>Est-ce que l'expérience se passe dehors ?</summary>
                <p>Oui, une partie de l'expérience se déroule en pleine nature. Certaines phases peuvent aussi se passer dans des espaces aménagés pour l'histoire, selon le déroulement de la session.</p>
            </details>

            <details class="faq-item">
                <summary>Que se passe-t-il en cas de mauvaise météo ?</summary>
                <p>Si la météo rend l'expérience difficile ou dangereuse, l'organisation peut adapter certaines activités, modifier le parcours ou proposer un report selon les conditions.</p>
            </details>
        </div>

        <div class="faq-block">
            <h2 class="faq-block-title">Préparation &amp; conditions</h2>

            <details class="faq-item">
                <summary>Est-ce qu'il faut être sportif ?</summary>
                <p>Non, l'expérience ne demande pas de condition physique particulière. Il faut simplement pouvoir marcher, rester debout par moments et évoluer en extérieur. Une tenue confortable est recommandée.</p>
            </details>

            <details class="faq-item">
                <summary>Que faut-il porter ?</summary>
                <p>Il est conseillé de venir avec une tenue confortable, adaptée à la météo et à une expérience en pleine nature. Des chaussures fermées sont fortement recommandées.</p>
            </details>

            <details class="faq-item">
                <summary>Les téléphones sont-ils autorisés ?</summary>
                <p>Non. L'objectif est d'être en immersion totale dans la peau d'un membre du Cercle. Les consignes exactes sont expliquées au début de l'expérience. En cas d'urgence, l'équipe reste disponible.</p>
            </details>

            <details class="faq-item">
                <summary>Combien de personnes peuvent participer ?</summary>
                <p>L'expérience est prévue pour des groupes de 2 à 6 personnes. Ce format permet de garder une immersion forte et une bonne interaction entre les participants et les figurants.</p>
            </details>
        </div>

        <div class="faq-block">
            <h2 class="faq-block-title">Repas &amp; restauration</h2>

            <details class="faq-item">
                <summary>Que mange-t-on pendant l'expérience ?</summary>
                <p>Un repas thématisé est prévu pendant l'expérience. Il est intégré à l'univers du Domaine afin de renforcer l'immersion et de donner l'impression de partager un moment avec la communauté.</p>
            </details>

            <details class="faq-item">
                <summary>Y a-t-il un petit-déjeuner ?</summary>
                <p>Un petit-déjeuner simple est prévu à la fin de l'expérience. Il permet de clôturer l'événement après la phase finale.</p>
            </details>

            <details class="faq-item">
                <summary>Peut-on signaler un régime alimentaire particulier ?</summary>
                <p>Oui. Les participants peuvent signaler leurs régimes alimentaires ou allergies au moment de la réservation. La totalité du repas est vegan pour correspondre à tous les régimes alimentaires.</p>
            </details>
        </div>

